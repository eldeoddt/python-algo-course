# 실전 예제 2 - 열차 교차 시간 알아내기
# 3개 노선 열차가 9-6 운행
# a:10 , b: 25, c: 30
# 10 20 30 40 50 60
# 25 50
# 30 60 -> 공배수 구하기
# 9-6 은 540분.
for i in range (1, 541, 1):
    if ( ((i%10==0) & (i%25==0))
    | ((i%25==0) & (i%30==0))
    | ((i%10==0) & (i%30==0))
    ):
        print("%d시 %d분" % ((i//60)+9, i%60))

